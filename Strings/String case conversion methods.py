text = 'hello dear'
print(text.capitalize())  # Output: 'Hello dear'

text = 'hello dear'
print(text.upper())  # Output: 'HELLO DEAR'

text = 'HELLO DEAR'
print(text.lower())  # Output: 'hello dear'

text = 'hello how are you'
print(text.title())  # Output: 'Hello How Are You'

text = 'Hello How Are You'
print(text.swapcase())  # Output: 'hELLO hOW aRE yOU'

text = 'Hello Dear'
print(text.casefold())  # Output: 'hello dear'